package codes.chapter15.class3;

import java.io.FileWriter;
import java.io.IOException;

public class FileWriterTest {
	public static void main(String[] args) throws IOException{
		FileWriter fw = null;
		
		try {
			fw = new FileWriter("poem.txt");
			//\r\n是window平台的换行，Unix/Linux/BSD等平台为\n即可
			fw.write("锦瑟-李商隐\r\n");
			fw.write("锦瑟无端五十弦，一弦一注思华年。\r\n");
			fw.write("庄生晓梦迷蝴蝶，望帝春心托杜鹃。\r\n");
			fw.write("沧海明月珠有泪，蓝田日暖玉生烟。\r\n");
			fw.write("此情可待成追忆，只是当时 已惘然。\r\n");
		} catch (IOException e) {
			e.printStackTrace();
		}finally{
			//使用finally块来关闭文件输出流
			if (fw != null) {
				fw.close();
			}
		}
		
	}
}
